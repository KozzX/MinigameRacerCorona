function posX( x )
	local vX = display.contentWidth / 16 * x -1;
	return vX
end

function posY( y )
	local vY = display.contentHeight / 25 * y -1;
	return vY
end

function tamX( x )
	local vX = display.contentWidth / 16 * x + 2;
	return vX
end

function tamY( y )
	local vY = display.contentHeight / 25 * y + 2;
	return vY
end




